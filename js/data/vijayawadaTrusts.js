// HELPER FUNCTIONS
// ═══════════════════════════════════════════════════════════════

/**
 * Get a trust by ID
 */
export function getTrustById(trustId) {
  return VIJAYAWADA_TRUSTS.find(t => t.id === trustId);
}

/**
 * Get all trusts by category
 */
export function getTrustsByCategory(category) {
  if (category === 'All') return VIJAYAWADA_TRUSTS;
  return VIJAYAWADA_TRUSTS.filter(t => t.category === category);
}

/**
 * Search trusts by name or description
 */
export function searchTrusts(query) {
  const q = query.toLowerCase();
  return VIJAYAWADA_TRUSTS.filter(t =>
    t.name.toLowerCase().includes(q) ||
    t.shortName.toLowerCase().includes(q) ||
    t.description.toLowerCase().includes(q)
  );
}

/**
 * Get verified trusts only
 */
export function getVerifiedTrusts() {
  return VIJAYAWADA_TRUSTS.filter(t => t.verified);
}

/**
 * Get trusts that accept specific donation types
 */
export function getTrustsByDonationType(donationType) {
  return VIJAYAWADA_TRUSTS.filter(t =>
    t.acceptsDonations.includes(donationType)
  );
}
